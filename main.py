def calculate_personal_year(birth_date_str: str, current_year: str) -> int:
    """–†–∞—Å—á–µ—Ç –õ–∏—á–Ω–æ–≥–æ –≥–æ–¥–∞ (–î–î+–ú–ú+–ì–ì–ì–ì)"""
    # –î–µ–Ω—å –∏ –º–µ—Å—è—Ü —Ä–æ–∂–¥–µ–Ω–∏—è
    day_month_part = birth_date_str.split('.')[:2]
    # –í—Å–µ —Ü–∏—Ñ—Ä—ã: –î–î + –ú–ú + –ì–ì–ì–ì
    all_digits = "".join(day_month_part) + current_year
    return reduce_to_single_digit(all_digits)

