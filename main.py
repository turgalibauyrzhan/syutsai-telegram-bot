def reduce_to_single_digit(number_str: str) -> int:
    """–°—É–º–º–∏—Ä—É–µ—Ç –≤—Å–µ —Ü–∏—Ñ—Ä—ã —á–∏—Å–ª–∞ –∏–ª–∏ —Å—Ç—Ä–æ–∫–∏ –¥–æ –æ–¥–Ω–æ–∑–Ω–∞—á–Ω–æ–≥–æ —á–∏—Å–ª–∞ (1-9)."""
    # –£–¥–∞–ª—è–µ–º —Ç–æ—á–∫–∏ –∏ —É–±–µ–∂–¥–∞–µ–º—Å—è, —á—Ç–æ —Ä–∞–±–æ—Ç–∞–µ–º —Ç–æ–ª—å–∫–æ —Å —Ü–∏—Ñ—Ä–∞–º–∏
    cleaned_str = "".join(filter(str.isdigit, number_str))
    
    if not cleaned_str:
        return 0

    current_sum = sum(int(digit) for digit in cleaned_str)
    
    while current_sum > 9 and current_sum != 11 and current_sum != 22:
        # –î–ª—è –Ω—É–º–µ—Ä–æ–ª–æ–≥–∏–∏ –°—é—Ü–∞–π –≤—Å–µ –ø—Ä–∏–≤–æ–¥–∏—Ç—Å—è –∫ 1-9. 11 –∏ 22 –Ω–µ –º–∞—Å—Ç–µ—Ä-—á–∏—Å–ª–∞
        current_sum = sum(int(digit) for digit in str(current_sum))
    
    # –í –¥–∞–Ω–Ω–æ–π —Å–∏—Å—Ç–µ–º–µ –≤—Å–µ –ø—Ä–∏–≤–æ–¥–∏–º –∫ 1-9 (11 -> 2, 22 -> 4).
    # –ù–æ –¥–ª—è —Ä–∞—Å—á–µ—Ç–æ–≤ –≥–æ–¥–∞, –º–µ—Å—è—Ü–∞ –∏ –¥–Ω—è –æ–±—ã—á–Ω–æ —Å–≤–æ–¥—è—Ç –¥–∞–∂–µ 11 –∏ 22
    while current_sum > 9:
        current_sum = sum(int(digit) for digit in str(current_sum))
        
    return current_sum

