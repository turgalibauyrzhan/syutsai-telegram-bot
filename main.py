    """–†–∞—Å—á–µ—Ç –õ–∏—á–Ω–æ–≥–æ –º–µ—Å—è—Ü–∞ (–õ–∏—á–Ω—ã–π –≥–æ–¥ + –ú–µ—Å—è—Ü)"""
